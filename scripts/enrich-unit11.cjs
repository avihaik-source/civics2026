let replacements = 0;

enrichments.forEach(e => {
  // Find the simple concept by term
  const termEsc = e.term.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  
  // Build enriched content object
  const enrichedContent = {
    definition: e.definition,
    mainPoint: e.mainPoint,
    keyPoints: e.keyPoints,
    learningTip: e.learningTip,
    faq: e.faq,
    relatedConcepts: e.relatedConcepts
  };
  if (e.comparisonTable) enrichedContent.comparisonTable = e.comparisonTable;
  if (e.detailedExplanation) enrichedContent.detailedExplanation = e.detailedExplanation;
  if (e.examples) enrichedContent.examples = e.examples;

  // Pattern: find the concept object with this term and replace it
  // Old format: {"term": "...", "def": "...", "source": "..."}
  const pattern = new RegExp(
    `\\{\\s*"term":\\s*"${termEsc}"[^}]*\\}`,
    's'
  );
  
  const match = data.match(pattern);
  if (match) {
    const newObj = JSON.stringify({
      id: e.id,
      term: e.term,
      content: enrichedContent
    }, null, 6);
    
    data = data.replace(match[0], newObj);
    replacements++;
    console.log(`✅ Enriched: ${e.term}`);
  } else {
    console.log(`❌ Not found: ${e.term}`);
  }
});

// Save
fs.writeFileSync(dataPath, data, 'utf8');
