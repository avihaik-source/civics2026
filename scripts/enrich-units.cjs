// ===== MAIN: Load data.js, enrich, and write back =====
const dataPath = path.join(__dirname, '..', 'public', 'static', 'data.js');
const content = fs.readFileSync(dataPath, 'utf8');

// Parse the UNITS_DATA from data.js
const fn = new Function(content + '; return { UNITS_DATA };');
const { UNITS_DATA } = fn();

// Enrich each unit
UNITS_DATA.forEach(unit => {
  // Add practice questions
  if (PRACTICE_DATA[unit.id]) {
    unit.practice = PRACTICE_DATA[unit.id].questions;
  }
  
  // Add summary key concepts
  if (SUMMARY_DATA[unit.id]) {
    unit.keyConcepts = SUMMARY_DATA[unit.id];
  }
  
  // Enhance checklist if keyConcepts exist
  if (unit.keyConcepts && unit.keyConcepts.length > 0 && unit.checklist.length < unit.keyConcepts.length) {
    // Add missing checklist items
    const existing = new Set(unit.checklist);
    unit.keyConcepts.forEach(kc => {
      const item = `אני מכיר/ה את המושג: ${kc.term}`;
      if (!existing.has(item) && unit.checklist.length < 12) {
        unit.checklist.push(item);
      }
    });
  }
});

// Generate output
let output = '// ===== CIVICS 2026 - UNITS DATA (Enriched with practice + summary) =====\n';
output += '// Generated: ' + new Date().toISOString().split('T')[0] + '\n';
output += '// Contains: 16 units, practice questions (3-5 per unit), key concepts (8-10 per unit)\n\n';
output += 'const UNITS_DATA = ' + JSON.stringify(UNITS_DATA, null, 2) + ';\n';

fs.writeFileSync(dataPath, output, 'utf8');

// Stats
let totalPractice = 0, totalConcepts = 0;
UNITS_DATA.forEach(u => {
  const pCount = (u.practice || []).length;
  const kCount = (u.keyConcepts || []).length;
  totalPractice += pCount;
  totalConcepts += kCount;
  console.log(`Unit ${u.id}: ${u.title} - practice: ${pCount}, keyConcepts: ${kCount}`);
});
console.log(`\nTotal: ${totalPractice} practice questions, ${totalConcepts} key concepts across ${UNITS_DATA.length} units`);
console.log('File written:', dataPath, '(' + fs.statSync(dataPath).size.toLocaleString() + ' bytes)');
